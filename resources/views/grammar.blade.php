            <h3 class="text-space">8. Герундий после глагола «like»</h3>
        </div>
        <p class="text-space">Герундий — это глагол с окончанием «ing». Если нужно выразить мысль о своих предпочтениях при помощи глагола «like», то после него предпочтительно использовать именно герундий:
        </p>
        <em class="text-main">I like watching films.
            Do you like jogging?
            You don't like playing chess, do you?
        </em>

