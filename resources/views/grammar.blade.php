            <h3 class="text-space">1. Порядок слов в предложении</h3>
        </div>
        <p class="text-space">Русская речь позволяет составлять фразы в любом порядке, в котором нам вздумается. Предложения «Он купил машину», «Машину купил он», «Купил он машину» и другие вариации будут звучать одинаково органично и правильно с точки зрения грамматики. Но англичане — народ педантичный, поэтому в их предложениях, так же как и в жизни, существует строго соблюдаемый порядок.</p>
        <p class="text-main">Подлежащее (кто?) + сказуемое (что делает?) + дополнение (с кем? над кем?) + обстоятельство (когда? где? каким образом?)</p>
        <em class="text-space">Он купил машину. — He bought a car.</em>
        <p class="text-space"> Второстепенные члены могут отсутствовать, но наличие подлежащего и сказуемого обязательно, поэтому существование безличных предложений в английском языке невозможно. Если в русском аналоге субъект, выполняющий действие отсутствует, то в английском его заменяет местоимение «it».</p>
        <em class="text-space">На улице холодно. — It's cold outside.</em>

        <div class="grammar-title">
            <img class="circle" src="/img/red-circle.png" alt="">
            <h3 class="text-space">2. Вспомогательные глаголы</h3>
        </div>
        <p class="text-space">Очень важно помнить о необходимости использовать вспомогательные глаголы в отрицательных и вопросительных предложениях. В русском языке нам не нужны никакие помощники, кроме основного глагола-сказуемого. Но в классическом английском языке построение вопроса и отрицания требует непременного наличия глагола-помощника.</p>
        <em class="text-main">Ты любишь музыку? — Do you like music?
            <br/>
            Я не пойду на эту вечеринку. — I won't (will not) go to the party.</em>
        <p class="text-space">Какой именно вспомогательный глагол следует использовать, зависит от времени.</p>

        <div class="grammar-title">
            <img class="circle" src="/img/red-circle.png" alt="">
            <h3 class="text-space">3. Формы глаголов be, have, do</h3>
        </div>
        <p class="text-space">Существует в английском языке «святая троица» глаголов — слова, которыми можно выразить большую часть действий. Это глаголы: «be» (быть, являться, находиться), «have» (иметь) и «do» (делать). Ввиду столь частого употребления, в настоящем времени у них появились свои особые формы.</p>
        <p class="text-main">
            «Be» разделилось на «am» (для I), «is» (для he, she, it) и «are» (для we, you, they).
            <br/>
            «Have» имеет особую форму только для третьего лица единственного числа (he, she, it) — «has».
            <br/>
            А «do», пользуясь общим правилом для «he», «she», «it» в настоящем времени, использует окончание «es» — «does».
        </p>
        <p class="text-space">Запомнив эти правила о глаголах и их формах, уже можно выразить свои мысли и объяснить иностранцу, что вам от него нужно.
        </p>

        <div class="grammar-title">
            <img class="circle" src="/img/red-circle.png" alt="">
            <h3 class="text-space">4. Запрет на двойное отрицание</h3>
        </div>
        <p class="text-space">Знаменитая игра «Я никогда не…» на английском называется «I've never (done)…». Как видно, в русском языке присутствует двойное отрицание — местоимение «никогда» и отрицательная частица «не». В английском мы наблюдаем только отрицательное наречие «never», а перед глаголом «done» привычной отрицательной частицы «not» нет и быть не может. Почему так сложилось и кто в этом виноват — истории неизвестно, но нам нравится версия о том, что деловые жители Туманного Альбиона просто не любят повторяться. А нам следует запомнить, что в английском языке нельзя использовать двойное отрицание.
        </p>

        <div class="grammar-title">
            <img class="circle" src="/img/red-circle.png" alt="">
            <h3 class="text-space">5. Артикли</h3>
        </div>
        <p class="text-space">Об этих таинственных ребятах можно говорить часами. Особенно долгим этот разговор делает тот факт, что в русском языке артиклей нет. Но мы не станем повторять многотомный материал учебников, а сконцентрируемся лишь на том, когда артикли не нужны.
        </p>
        <p class="text-space">Когда перед существительным стоит притяжательное местоимение или существительное в притяжательном падеже.
        </p>
        <em class="text-main">This is a dog. This is my dog. It isn't my sister's dog. — Это собака. Это моя собака. Это не собака моей сестры.
        </em>
        <p class="text-space">Когда перед существительным стоит количественное числительное (отвечает на вопрос «сколько?»).
        </p>
        <em class="text-main">I have two sisters and one brother. — У меня две сестры и один брат.
        </em>
        <p class="text-space">Когда перед существительным стоит отрицание «no».
        </p>
        <em class="text-main">I have no idea what to do. — Понятия не имею, что делать.</em>
        <p class="text-space">Когда перед существительным стоит указательное местоимения (this, these, that, those).
        </p>
        <em class="text-main">Give me that pencil, please. — Дай мне тот карандаш, пожалуйста.</em>

        <div class="grammar-title">
            <img class="circle" src="/img/red-circle.png" alt="">
            <h3 class="text-space">6. Множественное число</h3>
        </div>
        <p class="text-space">Основное правило образования множественного числа — это добавление окончания «s» к форме единственного числа.
        </p>
        <em class="text-main">a dog — dogs, a cat — cats, an insect — insects
        </em>
        <p class="text-space">Если существительное оканчивается на шипящий звук или букву «о», то следует добавить окончание «es».
        </p>
        <em class="text-main">bus — buses, glass — glasses, bush — bushes, box — boxes, branch — branches, potato –potatoes
        </em>
        <p class="text-space">В случае, когда слово заканчивается на «y», а перед ней стоит согласная, «y» во множественном числе меняется на «ies».
        </p>
        <em class="text-main">baby — babies, city — cities, lady — ladies
            <br/>
            Но: boy — boys, toy — toys, play — plays
        </em>
        <p class="text-space">Когда в конце слова стоит «f» или «fe», при добавлении окончания «s» буква «f» меняется на «v».
        </p>
        <em class="text-main">leaf — leaves, wife — wives
        </em>
        <p class="text-space">Это основные английские правила образования формы множественного числа, но не следует забывать об исключениях, которые идут вразрез с правилами, например:
        </p>
        <em class="text-main">
            child — children, man — men, mouse — mice
        </em>

