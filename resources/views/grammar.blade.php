            <h3 class="text-space">9. Формы прошедшего времени глаголов</h3>
        </div>
        <p class="text-space">Основное правило гласит, что для выражения прошедшего времени следует использовать вторую форму глагола. Тут начинаются нюансы, ведь английские глаголы подразделяются на правильные и неправильные, и способы образования второй формы у них отличаются. Для правильных глаголов используется окончание «ed», но каждый неправильный глагол имеет свои три формы, которые необходимо запомнить — отдельного правила для неправильных глаголов не существует. К счастью, многие из них образованы по схожим словообразовательным моделям, а современная методика обучения располагает забавными стишками, направленными на быстрое запоминание форм неправильных глаголов. Наш онлайн репетитор с радостью познакомит вас с ними.
        </p>

