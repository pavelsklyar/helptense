            <h3 class="text-space">10. Many, much, a lot</h3>
        </div>
        <p class="text-space">Существительные английского языка, подобно русским, подразделяют на два класса: исчисляемые и неисчисляемые. Класс влияет на использование количественных местоимений:
        </p>
        <p class="text-space">С исчисляемыми следует использовать «many».
        </p>
        <em class="text-main">I have many dresses. — У меня много платьев.
        </em>
        <p class="text-space">С неисчисляемыми, которые представляют собой жидкости, вещества и субстанции, употребляем «much».
        </p>
        <em class="text-main">I don't drink much water. — Я не пью много воды.
        </em>
        <p class="text-space">Если определить класс затруднительно, то можно воспользоваться «a lot (of)», которое одинаково хорошо сочетается как с неисчисляемыми существительными, так и с теми, которые можно посчитать.
        </p>
        <em class="text-main">He doesn't watch a lot of TV, but he reads a lot of books. — Он не смотрит телевизор много, но он читает много книг.
        </em>
        <p class="text-space">Небольшой подсказкой в определении класса существительных может служить окончание «s» у исчисляемых существительных во множественном числе.
        </p>

