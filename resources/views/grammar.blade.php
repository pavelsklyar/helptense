            <h3 class="text-space">7. Степени сравнения</h3>
        </div>
        <p class="text-space">Существует два способа образования степеней сравнения: при помощи суффиксов и при помощи дополнительных слов. На выбор способа влияет количество слогов и последняя буква в слове.
        </p>
        <p class="text-space">Если в слове один слог, то следует добавить суффикс.
        </p>
        <em class="text-main">cold — colder — the coldest, cool — cooler — the coolest, big — bigger — the bigges
        </em>
        <p class="text-space">Если слово состоит из двух и более слогов, то используем дополнительное слово.
        </p>
        <em class="text-main">beautiful — more beautiful — the most beautiful
        </em>
        <p class="text-space">Когда слово заканчивается на «y», мы снова используем первый способ с окончаниями, но при этом «y» меняется на «ie».
        </p>
        <em class="text-main">funny — funnier — the funniest, sunny — sunnier — the sunniest</em>
        <p class="text-space">Не забывайте артикль «the» перед превосходной степенью сравнения, а также удвоение последней согласной буквы в односложных словах с чередованием «согласная / гласная / согласная».
        </p>

        <div class="grammar-title">
